def _normalize(s):
    s = s.lower().strip()
    for suffix in [" jee", " jii", " ji", "jee", "jii"]:
        if s.endswith(suffix):
            s = s[: -len(suffix)].strip()
            break
    return " ".join(s.split())


COMMON_NAMES_LOOKUP = {_normalize(n): n for n in COMMON_NAMES}
